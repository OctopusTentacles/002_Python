# Контейнеры на складе лежат в ряд в порядке невозрастания 
# (меньше либо равно) массы в килограммах. На склад привезли 
# ещё один контейнер, который тоже нужно положить на определённое место.

# Напишите программу, которая получает на вход невозрастающую последовательность 
# натуральных чисел. Они означают массу каждого контейнера в ряду. 
# После этого вводится число X — масса нового контейнера. 
# Программа выводит номер, под которым будет лежать новый контейнер. 
# Если в ряде есть контейнеры с массой, как у нового, то его нужно положить после них.

# Обеспечьте контроль ввода: все числа не превышают 200.

# Пример:

# Количество контейнеров: 8
# Введите вес контейнера: 165 
