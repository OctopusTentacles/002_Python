def word_money(price):
    if 5 <= price <= 20 or 5 <= price % 100 <= 20:
        return '—Ä—É–±–ª–µ–π'
    elif price % 10 == 1:
        return '—Ä—É–±–ª—å'
    elif price % 10 in (2, 3, 4):
        return '—Ä—É–±–ª—è'
    else:
        return '—Ä—É–±–ª–µ–π'
    
